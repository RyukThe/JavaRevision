public class swap2Numberwo3Number {

	
	public static void main(String[] args) {
		
		
		int first=15;
		int second=20;
		System.out.println(first);
		System.out.println(second);
		
	
		first=first+second;//35
		System.out.println(first);
		second=first-second;//35-20=15
		System.out.println(second);
		first=first-second;//35-15=20
		System.out.println(first);
		
		System.out.println(first);
		System.out.println(second);
		
	}
}
